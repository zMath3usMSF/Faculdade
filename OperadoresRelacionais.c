#include <stdio.h>

int main(){
    int numero1 = 10;
    float numero2 = 10.5;

    printf("Número 1 > Número 2: %d\n", numero1 > numero2);
    printf("Número 1 >= Número 2: %d\n", numero1 >= numero2);
    printf("Número 1 < Número 2: %d\n", numero1 < numero2);
    printf("Número 1 <= Número 2: %d\n", numero1 < numero2);
    printf("Número 1 == Número 2: %d\n", numero1 == numero2);
    printf("Número 1 != Número 2: %d\n", numero1 != numero2);

    return 0;
}